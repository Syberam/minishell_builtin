#include "libft.h"

size_t		ft_ilen(int c)
{
	size_t	len;

	if (c <= 0)
	{
		len = 1;
		while (c != 0)
		{
			c = c / 10;
			len++;
		}
	}
	else
	{
		len = 0;
		while (c != 0)
		{
			c = c / 10;
			len++;
		}
	}
	return (len);
}
